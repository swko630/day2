     DML 
     1. Data Manipulation Launguage 데이터 조작어
     2. 데이터(행,Row)를 다루는 언어이다.
     3. 트랜잭션 처리가 필요하다.
     4. 종류
          1) 행 삽입 : INSERT INTO VALUES
          2) 행 수정 : UPDATE SET WHERE
          3) 행 삭제 : DELETE FROM WHERE
*/
/*
     트랜잭션
     1. 한 번에 수행해야 하는 작업을 의미한다. (원자성, All or Nothing)
     2. 은행 이체처럼 2가지 이상의 작업이 하나의 트랜잭션으로 구성된다.
     3. 작업의 저장을 의미하는 COMMIT과 작업의 취소를 의미하는 ROLLBACK이 필요하다 - COMMIT 과 ROLLBACK은 TCL이라고도 부름
     4. 예시 : 은행 이체
          1) 내 통장에서 돈 빼기
          2) 너 통장으로 돈 넣기
     5. 종류
          1) 행 삽입 : INSERT INTO VALUES
          2) 행 수정 : UPDATE SET WHERE
          3) 행 삭제 : DELETE FROM WHERE
*/

